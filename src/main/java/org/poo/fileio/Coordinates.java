package org.poo.fileio;

public final class Coordinates {
    private int x, y;

    /**
     * Constructor
     */
    public Coordinates() {
    }

    /**
     * @return Getter for x
     */
    public int getX() {
        return x;
    }

    /**
     * @param x Setter for x
     */
    public void setX(final int x) {
        this.x = x;
    }

    /**
     * @return Getter for y
     */
    public int getY() {
        return y;
    }

    /**
     * @param y Setter for y
     */
    public void setY(final int y) {
        this.y = y;
    }
}
